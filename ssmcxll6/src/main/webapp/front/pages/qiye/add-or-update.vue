				for (var o in obj){
					if(o=='zhanghao'){
					this.ruleForm.zhanghao = obj[o];
					this.ro.zhanghao = true;
					continue;
					}
					if(o=='mima'){
					this.ruleForm.mima = obj[o];
					this.ro.mima = true;
					continue;
					}
					if(o=='qiyemingcheng'){
					this.ruleForm.qiyemingcheng = obj[o];
					this.ro.qiyemingcheng = true;
					continue;
					}
